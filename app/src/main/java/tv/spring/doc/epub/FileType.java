package tv.spring.doc.epub;

/**
 * FileType is an enumeration that represents different types of file formats.
 * <p>
 * HTML - Represents HTML code.
 * <p>
 * EPUB - Represents an EPUB file format. EPUB is a standard file format for representing electronic publications,
 * including e-books and digital magazines.
 */
public enum FileType {
    /**
     * The HTML enum represents HTML code.
     */
    HTML,
    /**
     * The EPUB class represents an EPUB file.
     */
    EPUB
}
